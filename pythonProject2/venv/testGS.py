str_1 = "123"
str_2 = "Abc"
str_3 = "123Abc"

#用isdigit函数判断是否数字
print(str_1.isdigit())

print(str_2.isdigit())

print(str_3.isdigit())


#用isalpha判断是否字母
print(str_1.isalpha())

print(str_2.isalpha())

print(str_3.isalpha())

#isalnum判断是否数字和字母的组合
print(str_1.isalnum())

print(str_2.isalnum())

print(str_1.isalnum())
